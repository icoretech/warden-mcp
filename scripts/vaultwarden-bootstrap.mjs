import fs from 'node:fs/promises';
import path from 'node:path';
import { chromium } from 'playwright';

function mustEnv(name) {
  const v = process.env[name];
  if (!v) throw new Error(`Missing env ${name}`);
  return v;
}

const baseUrl = mustEnv('VW_BASE_URL').replace(/\/$/, '');
const email = mustEnv('BW_USER');
const password = mustEnv('BW_PASSWORD');
const name = process.env.VW_NAME ?? 'Keychain Test';
const orgName = (process.env.VW_ORG_NAME ?? '').trim();
const artifactsDir = path.resolve(
  process.cwd(),
  'tmp',
  'vaultwarden-bootstrap',
);
const apiKeyEnvFile = (process.env.VW_APIKEY_ENV_FILE ?? '').trim();
const timeoutMs = Number.parseInt(
  process.env.VW_BOOTSTRAP_TIMEOUT_MS ?? '10000',
  10,
);

function toShLiteral(value) {
  return `'${value.replace(/'/g, `'"'"'`)}'`;
}

async function main() {
  const base = new URL(baseUrl);
  const chromiumArgs = [];
  // Vaultwarden's bundled web vault requires a secure context for SubtleCrypto.
  // When running inside docker-compose we typically access it via http://vaultwarden,
  // which is NOT considered a secure context by Chromium. This flag makes Chromium
  // treat it as secure so the registration form works in headless automation.
  if (
    base.protocol === 'http:' &&
    base.hostname !== 'localhost' &&
    base.hostname !== '127.0.0.1' &&
    base.hostname !== '::1'
  ) {
    chromiumArgs.push(
      `--unsafely-treat-insecure-origin-as-secure=${base.origin}`,
    );
  }

  const browser = await chromium.launch({ headless: true, args: chromiumArgs });
  const page = await browser.newPage({ ignoreHTTPSErrors: true });
  page.setDefaultTimeout(timeoutMs);
  page.setDefaultNavigationTimeout(timeoutMs);

  async function saveArtifacts(tag) {
    await fs.mkdir(artifactsDir, { recursive: true });
    const safeTag = tag.replace(/[^a-zA-Z0-9._-]/g, '_').slice(0, 64);
    const pngPath = path.join(artifactsDir, `${safeTag}.png`);
    const htmlPath = path.join(artifactsDir, `${safeTag}.html`);
    await page.screenshot({ path: pngPath, fullPage: true }).catch(() => {});
    const html = await page.content().catch(() => '');
    await fs.writeFile(htmlPath, html, 'utf8').catch(() => {});
    console.log(`[bootstrap] saved artifacts: ${pngPath} ${htmlPath}`);
  }

  async function clearApiKeyEnvFile() {
    if (!apiKeyEnvFile) return;
    await fs
      .rm(path.resolve(process.cwd(), apiKeyEnvFile), {
        force: true,
      })
      .catch(() => {});
  }

  async function writeApiKeyEnv(clientId, clientSecret) {
    if (!apiKeyEnvFile) return;
    const filePath = path.resolve(process.cwd(), apiKeyEnvFile);
    await fs.mkdir(path.dirname(filePath), { recursive: true });
    await fs.writeFile(
      filePath,
      [
        '# generated by scripts/vaultwarden-bootstrap.mjs',
        `BW_CLIENTID=${toShLiteral(clientId)}`,
        `BW_CLIENTSECRET=${toShLiteral(clientSecret)}`,
        '',
      ].join('\n'),
      { encoding: 'utf8', mode: 0o600 },
    );
    console.log(`[bootstrap] wrote api key env: ${filePath}`);
  }

  async function gotoAndWait(url) {
    await page.goto(url, { waitUntil: 'domcontentloaded' });
    await page.waitForTimeout(100);
  }

  function escapeRegExp(s) {
    return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  }

  async function clickFirstVisible(candidates) {
    for (const loc of candidates) {
      const n = await loc.count();
      for (let i = 0; i < n; i += 1) {
        const el = loc.nth(i);
        if (await el.isVisible().catch(() => false)) {
          await el.click();
          return true;
        }
      }
    }
    return false;
  }

  async function fillFirstVisible(candidates, value) {
    for (const loc of candidates) {
      const n = await loc.count();
      for (let i = 0; i < n; i += 1) {
        const el = loc.nth(i);
        if (await el.isVisible().catch(() => false)) {
          await el.fill(value);
          return true;
        }
      }
    }
    return false;
  }

  async function ensureOnRegister() {
    // Best-effort: try direct hash route first; if the SPA doesn't render the form,
    // fallback to navigating via the login page and clicking a visible "Create account" link.
    // Vaultwarden's web vault uses `#/signup` (and older builds may still support `#/register`).
    await gotoAndWait(`${baseUrl}/#/signup`);

    const emailCandidates = [
      page.locator('input[formcontrolname="email"]'),
      page.locator('input[autocomplete="email"]'),
      page.locator('input[type="email"]'),
      page.locator('input[name="email"]'),
      page.locator('input#email'),
      page.getByLabel(/email/i),
    ];

    for (const loc of emailCandidates) {
      if (
        (await loc.count()) &&
        (await loc
          .first()
          .isVisible()
          .catch(() => false))
      ) {
        return 'register';
      }
    }

    // Fallback: login page -> click create/register link/button.
    await gotoAndWait(`${baseUrl}/#/login`);
    await clickFirstVisible([
      page.getByRole('link', {
        name: /create account|register|sign up|create an account/i,
      }),
      page.getByRole('button', {
        name: /create account|register|sign up|create an account/i,
      }),
    ]);

    await page.waitForLoadState('networkidle').catch(() => {});

    for (const loc of emailCandidates) {
      if (
        (await loc.count()) &&
        (await loc
          .first()
          .isVisible()
          .catch(() => false))
      ) {
        return 'register';
      }
    }

    const loginPasswordVisible = await page
      .locator(
        "input[formcontrolname='masterPassword'], input[formcontrolname='password'], input[autocomplete='current-password']",
      )
      .first()
      .isVisible()
      .catch(() => false);
    if (loginPasswordVisible || /#\/login\b/.test(page.url())) {
      return 'login';
    }

    throw new Error('Could not reach Vaultwarden registration flow');
  }

  async function ensureLoggedIn() {
    await gotoAndWait(`${baseUrl}/#/login`);

    const alreadyAuthenticated =
      /#\/(vault|home|tabs|settings|setup-extension)\b/.test(page.url());

    const passwordCandidates = [
      page.locator(
        "input[formcontrolname='masterPassword'], input[formcontrolname='password']",
      ),
      page.locator('input[autocomplete="current-password"]'),
      page.locator('input[type="password"]'),
      page.getByLabel(/master password/i),
      page.getByLabel(/^password$/i),
    ];

    if (!alreadyAuthenticated) {
      // Some builds land directly on the master password step (email pre-filled/hidden).
      const pwAlreadyVisible = await passwordCandidates[0]
        .nth(0)
        .isVisible()
        .catch(() => false);

      // Newer Vaultwarden Web builds split login into two steps:
      // 1) enter email
      // 2) click "Log in with master password" and then enter password
      if (!pwAlreadyVisible) {
        // Email step
        const emailOk = await fillFirstVisible(
          [
            page.locator("input[formcontrolname='email']"),
            page.locator('input[autocomplete="email"]'),
            page.locator('input[type="email"]'),
            page.locator('input[name="email"]'),
            page.locator('input#email'),
            page.getByLabel(/email address/i),
            page.getByLabel(/email/i),
          ],
          email,
        );
        if (!emailOk) throw new Error('Could not find login email input');

        await clickFirstVisible([
          page.getByRole('button', { name: /log in with master password/i }),
          page.getByRole('button', { name: /continue|next/i }),
          page.getByRole('button', { name: /log in|login|sign in/i }),
        ]);
        await Promise.race([
          passwordCandidates[0]
            .nth(0)
            .waitFor({ state: 'visible' })
            .catch(() => {}),
          page.waitForTimeout(timeoutMs),
        ]);
      }

      const pwOk = await fillFirstVisible(passwordCandidates, password);
      if (!pwOk) throw new Error('Could not find login password input');

      const clicked = await clickFirstVisible([
        page.getByRole('button', { name: /log in|login|sign in/i }),
        page.getByRole('button', { name: /continue/i }),
      ]);
      if (!clicked) throw new Error('Could not find login submit button');

      const loggedIn = await page
        .waitForURL(/#\/(vault|home|tabs|settings|setup-extension)/, {
          timeout: timeoutMs,
        })
        .then(() => true)
        .catch(() => false);
      if (!loggedIn) {
        const bodyText = await page
          .locator('body')
          .innerText()
          .catch(() => '');
        throw new Error(
          `Login did not complete within ${timeoutMs}ms. Still at ${page.url()}.\n` +
            bodyText.slice(0, 500),
        );
      }
    }

    // Some first-login flows land on setup-extension and block settings routes
    // until you go through the confirmation modal.
    if (/#\/setup-extension\b/.test(page.url())) {
      await page.waitForTimeout(300);

      // 1) "Add it later" (opens confirmation dialog)
      await clickFirstVisible([
        page.getByRole('button', { name: /add it later/i }),
      ]).catch(() => {});
      await page.waitForTimeout(250);

      // 2) confirm dialog "Skip to web app" (navigates to /#/vault)
      await clickFirstVisible([
        page.getByRole('link', { name: /skip to web app/i }),
        page.getByRole('button', { name: /skip to web app/i }),
      ]).catch(() => {});

      await page
        .waitForURL(/#\/(vault|home|tabs|settings)/, { timeout: timeoutMs })
        .catch(() => {});
      if (/#\/setup-extension\b/.test(page.url())) {
        throw new Error('Could not get past setup-extension (skip-to-web-app)');
      }
    }

    // Best-effort: dismiss any post-login modals.
    await clickFirstVisible([
      page.getByRole('button', { name: /close/i }),
      page.getByRole('button', { name: /dismiss/i }),
      page.getByRole('button', { name: /skip/i }),
      page.getByRole('button', { name: /not now/i }),
    ]).catch(() => {});
  }

  async function ensureOrganization(targetName) {
    if (!targetName) return;

    await ensureLoggedIn();
    await gotoAndWait(`${baseUrl}/#/settings/organizations`);
    if (/#\/login\b/.test(page.url())) {
      throw new Error(
        'Expected to be authenticated, but was redirected to login',
      );
    }
    if (/#\/setup-extension\b/.test(page.url())) {
      throw new Error(
        'Could not reach organizations settings (blocked by setup-extension screen)',
      );
    }

    const targetRe = new RegExp(`\\b${escapeRegExp(targetName)}\\b`, 'i');
    const already = page.getByText(targetRe).first();
    if (await already.isVisible().catch(() => false)) {
      console.log('[bootstrap] organization already present, continuing');
      return;
    }

    const clickedNew = await clickFirstVisible([
      page.getByRole('button', {
        name: /new organization|create organization|add organization/i,
      }),
      page.getByRole('link', {
        name: /new organization|create organization|add organization/i,
      }),
      page.getByRole('button', { name: /new/i }).filter({
        hasText: /organization/i,
      }),
    ]);
    if (!clickedNew) {
      // Some vault builds use a nested settings nav; try to reach a "new" route directly.
      await gotoAndWait(`${baseUrl}/#/settings/organizations/new`);
    }

    const nameOk = await fillFirstVisible(
      [
        page.getByLabel(/organization name/i),
        page.getByLabel(/^name$/i),
        page.locator('input[name="name"]'),
        page.locator("input[formcontrolname='name']"),
      ],
      targetName,
    );
    if (!nameOk) throw new Error('Could not find organization name input');

    // Some wizards ask for a billing email; best-effort.
    await fillFirstVisible(
      [
        page.getByLabel(/billing email/i),
        page.locator('input[name="billingEmail"]'),
        page.locator("input[formcontrolname='billingEmail']"),
      ],
      email,
    ).catch(() => {});

    // Wizard steps vary by build; click through a few times until we see the org name appear
    // back on the organizations list page.
    for (let i = 0; i < 6; i += 1) {
      const done = await page
        .getByText(targetRe)
        .first()
        .isVisible()
        .catch(() => false);
      if (done) {
        console.log('[bootstrap] created organization');
        return;
      }

      const progressed = await clickFirstVisible([
        page.getByRole('button', { name: /create organization/i }),
        page.getByRole('button', { name: /create/i }).filter({
          hasText: /organization/i,
        }),
        page.getByRole('button', { name: /continue|next|submit|finish/i }),
        page.getByRole('button', { name: /done/i }),
      ]);

      if (!progressed) break;
      await page.waitForTimeout(250);
    }

    await gotoAndWait(`${baseUrl}/#/settings/organizations`);
    if (
      !(await page
        .getByText(targetRe)
        .first()
        .isVisible()
        .catch(() => false))
    ) {
      throw new Error(
        `Organization "${targetName}" did not appear after create flow`,
      );
    }

    console.log('[bootstrap] created organization');
  }

  async function exportApiKeyEnv() {
    if (!apiKeyEnvFile) return;

    await ensureLoggedIn();
    await gotoAndWait(`${baseUrl}/#/settings/security/security-keys`);

    const opened = await clickFirstVisible([
      page.getByRole('button', { name: /view api key/i }),
    ]);
    if (!opened) {
      throw new Error('Could not find "View API key" button');
    }

    await page.waitForTimeout(250);

    const filled = await fillFirstVisible(
      [
        page.locator('#masterPassword'),
        page.getByLabel(/master password/i),
        page.locator('input[type="password"]'),
      ],
      password,
    );
    if (!filled) {
      throw new Error('Could not find API key master password prompt');
    }

    const confirmed = await clickFirstVisible([
      page.getByRole('button', { name: /view api key/i }),
    ]);
    if (!confirmed) {
      throw new Error('Could not confirm API key reveal');
    }

    await Promise.race([
      page.getByText(/oauth 2\.0 client credentials/i).waitFor({
        state: 'visible',
      }),
      page.waitForTimeout(timeoutMs),
    ]);

    const bodyText = await page
      .locator('body')
      .innerText()
      .catch(() => '');
    const clientIdMatch = bodyText.match(/client_id:\s*([^\s]+)/i);
    const clientSecretMatch = bodyText.match(/client_secret:\s*([^\s]+)/i);

    if (!clientIdMatch || !clientSecretMatch) {
      throw new Error('Could not extract API key client credentials');
    }

    await writeApiKeyEnv(clientIdMatch[1], clientSecretMatch[1]);
  }

  try {
    await clearApiKeyEnvFile();

    let userAlreadyExists = false;
    const registerMode = await ensureOnRegister();
    if (registerMode === 'login') {
      console.log(
        '[bootstrap] landed on existing-account login flow, skipping registration',
      );
      userAlreadyExists = true;
    }

    const ctx = await page
      .evaluate(() => ({
        url: location.href,
        origin: location.origin,
        isSecureContext: globalThis.isSecureContext,
        hasSubtle: !!globalThis.crypto?.subtle,
      }))
      .catch(() => null);
    if (ctx) console.log(`[bootstrap] context: ${JSON.stringify(ctx)}`);

    // Step 1: email + name
    await page.getByLabel(/email address/i).fill(email);
    // Name is optional in some policies, but Vaultwarden's default web vault shows it.
    await page
      .getByLabel(/^name$/i)
      .fill(name)
      .catch(() => {});

    // Step 1 submit (usually "Continue")
    await page.getByRole('button', { name: /continue/i }).click();

    // Step 1 outcome:
    // - New user: navigate to password step (master password visible).
    // - Existing user: show an error and stay on step 1.
    // Vaultwarden can send us through either:
    // - auth-registration-start (formcontrolname=masterPassword, retypeMasterPassword)
    // - auth-registration-finish (formcontrolname=newPassword, newPasswordConfirm)
    const masterPw = page.locator(
      "input[formcontrolname='masterPassword'], input[formcontrolname='newPassword']",
    );
    const step1Error = page.getByText(
      /already (in use|exists)|email.*taken|account.*exists/i,
    );

    await Promise.race([
      masterPw.waitFor({ state: 'visible' }).catch(() => {}),
      step1Error
        .first()
        .waitFor({ state: 'visible' })
        .catch(() => {}),
      page.waitForTimeout(timeoutMs),
    ]);

    const step1BodyText = await page
      .locator('body')
      .innerText()
      .catch(() => '');
    if (
      /already (in use|exists)|email.*taken|account.*exists/i.test(
        step1BodyText,
      )
    ) {
      console.log('[bootstrap] user already exists (step1), continuing');
      userAlreadyExists = true;
    }

    if (!userAlreadyExists) {
      if (
        !(await masterPw
          .first()
          .isVisible()
          .catch(() => false))
      ) {
        throw new Error(
          `Signup did not advance to password step within ${timeoutMs}ms. If the container is slow, set VW_BOOTSTRAP_TIMEOUT_MS.`,
        );
      }

      // Step 2: master password + confirm
      await masterPw.first().fill(password);
      await page
        .locator(
          "input[formcontrolname='retypeMasterPassword'], input[formcontrolname='newPasswordConfirm']",
        )
        .first()
        .fill(password);

      // Avoid any external lookups in CI/test environments (best-effort).
      await page
        .getByRole('checkbox', { name: /check known data breaches/i })
        .uncheck()
        .catch(() => {});

      await page.getByRole('button', { name: /create account/i }).click();

      // Wait for either success navigation or an error message.
      // We don't want brittle selector matching; accept either.
      await Promise.race([
        page
          .waitForURL(/#\/(login|vault|home|setup-extension)/)
          .catch(() => {}),
        page.waitForTimeout(timeoutMs),
      ]);

      // If there's a visible error that implies already registered, that's fine.
      const bodyText = await page
        .locator('body')
        .innerText()
        .catch(() => '');
      if (/already (in use|exists)|email.*taken/i.test(bodyText)) {
        console.log('[bootstrap] user already exists, continuing');
      } else {
        console.log('[bootstrap] attempted registration');
      }
    }

    await ensureOrganization(orgName);
    await exportApiKeyEnv();
  } catch (err) {
    await clearApiKeyEnvFile();
    await saveArtifacts('failed');
    throw err;
  } finally {
    await browser.close().catch(() => {});
  }
}

main().catch((err) => {
  console.error(`[bootstrap] failed: ${err?.message ?? String(err)}`);
  process.exit(1);
});
